/*
*  cascade delete obs to x_min/x_max
*/ 
CREATE OR REPLACE FUNCTION delete_obs_trigger_fn()
  RETURNS TRIGGER LANGUAGE PLPGSQL AS
$BODY$
DECLARE
BEGIN
    delete from x_min where obs_id = OLD.id;
    delete from x_max where obs_id = OLD.id;
  return OLD;
END
$BODY$;

